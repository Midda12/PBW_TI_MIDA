  <p>
    Tahun 2025 membawa angin segar bagi dunia fashion dan lifestyle. Warna pastel
    ungu-biru kembali menjadi primadona, dipadukan dengan gaya minimalis yang
    tetap elegan. Banyak brand besar mulai fokus pada <i>sustainable fashion</i>,
    dengan penggunaan material ramah lingkungan yang mendukung gaya hidup hijau.
  </p>

  <p>
    Dalam dunia skincare, tren "skinimalism" semakin digemari. Konsumen kini lebih
    memilih rangkaian produk sederhana namun efektif untuk menjaga kesehatan kulit.
    Produk-produk dengan bahan alami seperti <i>niacinamide</i> dan <i>hyaluronic acid</i>
    menjadi favorit karena memberikan hasil optimal tanpa perlu banyak langkah.
  </p>

  <p>
    Makeup di tahun ini lebih menonjolkan sisi natural. Alih-alih riasan tebal,
    gaya "soft glam" menjadi pilihan utama. Fokusnya adalah mempertegas kecantikan
    alami dengan sentuhan ringan pada mata dan bibir.
  </p>

  <p>
    Dari sisi lifestyle, tren mindfulness dan produktivitas seimbang menjadi sorotan.
    Generasi muda semakin sadar pentingnya menjaga kesehatan mental sekaligus tetap
    aktif dalam berkarya. Aktivitas seperti yoga, journaling, dan digital detox mulai
    masuk ke rutinitas sehari-hari.
  </p>

  <p>
    Dengan kombinasi tren fashion, skincare, makeup, dan gaya hidup sehat, tahun 2025
    diyakini akan menjadi era di mana keindahan dan kesadaran diri berjalan seiring.
  </p>
